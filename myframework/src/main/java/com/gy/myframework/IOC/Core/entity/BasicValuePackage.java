public class BasicValuePackage {
    private int Int;
    private long Long;
    private float Float;
    private double Double;
    private char Char;
    private boolean Boolean;
    private byte Byte;

    public int getInt() {
        return Int;
    }

    public void setInt(int anInt) {
        Int = anInt;
    }

    public long getLong() {
        return Long;
    }

    public void setLong(long aLong) {
        Long = aLong;
    }

    public float getFloat() {
        return Float;
    }

    public void setFloat(float aFloat) {
        Float = aFloat;
    }

    public double getDouble() {
        return Double;
    }

    public void setDouble(double aDouble) {
        Double = aDouble;
    }

    public char getChar() {
        return Char;
    }

    public void setChar(char aChar) {
        Char = aChar;
    }

    public boolean isBoolean() {
        return Boolean;
    }

    public void setBoolean(boolean aBoolean) {
        Boolean = aBoolean;
    }

    public byte getByte() {
        return Byte;
    }

    public void setByte(byte aByte) {
        Byte = aByte;
    }
}
